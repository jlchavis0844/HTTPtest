package Reader;

import Model.StringObject;

public class IssueObject extends StringObject{
	 private String Issue_CoverDate;
	 private String Issue_Description;
	 private String Issue_ID;
	 private String Issue_Image;
	 private String Issue_Number;
	 private String Issue_Name;
	 private String Issue_SiteDetailUrl;
	 private String Issue_StoreDate;
	 
	public String getIssue_CoverDate() {
		return Issue_CoverDate;
	}
	public void setIssue_CoverDate(String issue_CoverDate) {
		Issue_CoverDate = issue_CoverDate;
	}
	public String getIssue_Description() {
		return Issue_Description;
	}
	public void setIssue_Description(String issue_Description) {
		Issue_Description = issue_Description;
	}
	public String getIssue_ID() {
		return Issue_ID;
	}
	public void setIssue_ID(String issue_ID) {
		Issue_ID = issue_ID;
	}
	public String getIssue_Image() {
		return Issue_Image;
	}
	public void setIssue_Image(String issue_Image) {
		Issue_Image = issue_Image;
	}
	public String getIssue_Number() {
		return Issue_Number;
	}
	public void setIssue_Number(String issue_Number) {
		Issue_Number = issue_Number;
	}
	public String getIssue_Name() {
		return Issue_Name;
	}
	public void setIssue_Name(String issue_Name) {
		Issue_Name = issue_Name;
	}
	public String getIssue_SiteDetailUrl() {
		return Issue_SiteDetailUrl;
	}
	public void setIssue_SiteDetailUrl(String issue_SiteDetailUrl) {
		Issue_SiteDetailUrl = issue_SiteDetailUrl;
	}
	public String getIssue_StoreDate() {
		return Issue_StoreDate;
	}
	public void setIssue_StoreDate(String issue_StoreDate) {
		Issue_StoreDate = issue_StoreDate;
	}
}
