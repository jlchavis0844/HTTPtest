package Model;

public class VolumeObj {
	private String Volume_ID;
	private String Volume_Name;
	private String Volume_CountofIssues;
	private String Volume_Description;
	private String Volume_Publisher;
	private String Volume_Start_Year;
	
	public String getVolume_ID() {
		return Volume_ID;
	}
	public void setVolume_ID(String volume_ID) {
		Volume_ID = volume_ID;
	}
	public String getVolume_Name() {
		return Volume_Name;
	}
	public void setVolume_Name(String volume_Name) {
		Volume_Name = volume_Name;
	}
	public String getVolume_CountofIssues() {
		return Volume_CountofIssues;
	}
	public void setVolume_CountofIssues(String volume_CountofIssues) {
		Volume_CountofIssues = volume_CountofIssues;
	}
	public String getVolume_Description() {
		return Volume_Description;
	}
	public void setVolume_Description(String volume_Description) {
		Volume_Description = volume_Description;
	}
	public String getVolume_Publisher() {
		return Volume_Publisher;
	}
	public void setVolume_Publisher(String volume_Publisher) {
		Volume_Publisher = volume_Publisher;
	}
	public String getVolume_Start_Year() {
		return Volume_Start_Year;
	}
	public void setVolume_Start_Year(String volume_Start_Year) {
		Volume_Start_Year = volume_Start_Year;
	}
}
